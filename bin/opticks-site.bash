opticks-site-check(){ 
   local func
   $FUNCNAME- | while read func ; do
      local path=$($func)
      local f=$(test -f $path && echo f || echo " ") 
      local d=$(test -d $path && echo d || echo " ") 
      local x=$(test -x $path && echo x || echo " ") 
      printf " %3s %-30s %s \n" "$f$d$x"  "$func" "$path" 
   done
}





